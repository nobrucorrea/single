    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        File arquivoConfiguracoesLocal = new File("config.properties");
        Properties propriedades = new Properties();
        String ambiente = obtemValorSelecionado(comboBoxAmbiente);
        switch (ambiente) {
            case AMBIENTE_PRODUCAO:
                propriedades.put(CHAVE_AMBIENTE, "producao");
                break;
            case AMBIENTE_DESENVOLVIMENTO:
                propriedades.put(CHAVE_AMBIENTE, "desenvolvimento");
                break;
        }
        String tipoEntrada = obtemValorSelecionado(comboBoxTipoEntrada);
        switch (tipoEntrada) {
            case TIPO_ENTRADA_XML:
                propriedades.put(CHAVE_TIPO_ENTRADA, "xml");
                String caminho = textoArquivo.getText();
                propriedades.put(CHAVE_XML_CAMINHO, caminho);
                break;
            case TIPO_ENTRADA_WEBSERVICE:
                propriedades.put(CHAVE_TIPO_ENTRADA, "webservice");
                String url = textoURL.getText();
                String id = textoID.getText();
                String token = textoToken.getText();
                propriedades.put(CHAVE_WEBSERVICE_URL, url);
                propriedades.put(CHAVE_WEBSERVICE_ID, id);
                propriedades.put(CHAVE_WEBSERVICE_TOKEN, token);
                break;
        }
        String modeloEntrada = obtemValorSelecionado(comboBoxModeloEntrada);
        switch (modeloEntrada) {
            case MODELO_ENTRADA_SOAP_FINAL_RJ:
                propriedades.put(CHAVE_MODELO_ENTRADA, "soap_final_rj");
                break;
            case MODELO_ENTRADA_MODELO_ANTIGO:
                propriedades.put(CHAVE_MODELO_ENTRADA, "modelo_antigo");
                break;
        }
        String modalidadeJuridica = obtemValorSelecionado(comboBoxModalidadeJuridica);
        switch (modalidadeJuridica) {
            case MODALIDADE_JURIDICA_SIMPLES:
                propriedades.put(CHAVE_MODALIDADE_JURIDICA, "simples");
                break;
            case MODALIDADE_JURIDICA_LUCRO_REAL:
                propriedades.put(CHAVE_MODALIDADE_JURIDICA, "lucro_real");
                break;
            case MODALIDADE_JURIDICA_LUCRO_PRESUMIDO:
                propriedades.put(CHAVE_MODALIDADE_JURIDICA, "lucro_presumido");
                break;
        }

        String emailServidor = textoServidor.getText();
        propriedades.put(CHAVE_EMAIL_SERVIDOR, emailServidor);
        String emailPorta = textoPorta.getText();
        propriedades.put(CHAVE_EMAIL_PORTA, emailPorta);
        String emailUsuario = textoUsuario.getText();
        propriedades.put(CHAVE_EMAIL_USUARIO, emailUsuario);
        String emailSenha = textoSenha.getText();
        propriedades.put(CHAVE_EMAIL_SENHA, emailSenha);
        String emailDestinatario = textoDestinatario.getText();
        propriedades.put(CHAVE_EMAIL_DESTINATARIO, emailDestinatario);

        String sistema = obtemValorSelecionado(comboBoxSistema);
        String bancoURL = textoStringDeConexao.getText();
        String bancoUsuario = textoUsuarioBD.getText();
        String bancoSenha = textoSenhaBD.getText();
        switch (sistema) {
            case RETAGUARDA_ALTERDATA_POSTGRESQL:
                propriedades.put(CHAVE_HIBERNATE_DIALECT, RETAGUARDA_ALTERDATA_POSTGRESQL_DIALECT);
                propriedades.put(CHAVE_HIBERNATE_DRIVER, RETAGUARDA_ALTERDATA_POSTGRESQL_DRIVER);
                propriedades.put(CHAVE_HIBERNATE_URL, bancoURL);
                propriedades.put(CHAVE_HIBERNATE_USERNAME, bancoUsuario);
                propriedades.put(CHAVE_HIBERNATE_PASSWORD, bancoSenha);
                propriedades.put(CHAVE_RETAGUARDA, RETAGUARDA_ALTERDATA_POSTGRESQL_RETAGUARDA);

                 {
                    String cfops = textoCFOPs.getText();
                    propriedades.put(CHAVE_ALTERDATA_CFOPS, cfops);
                    String estadosEntrada = textoEstadosEntrada.getText();
                    propriedades.put(CHAVE_ALTERDATA_ESTADOS_ENTRADA, estadosEntrada);
                    String estadosSaida = textoEstadosSaida.getText();
                    propriedades.put(CHAVE_ALTERDATA_ESTADOS_SAIDA, estadosSaida);
                }
                break;
            case RETAGUARDA_ALTERDATA_SQLSERVER:
                propriedades.put(CHAVE_HIBERNATE_DIALECT, RETAGUARDA_ALTERDATA_SQLSERVER_DIALECT);
                propriedades.put(CHAVE_HIBERNATE_DRIVER, RETAGUARDA_ALTERDATA_SQLSERVER_DRIVER);
                propriedades.put(CHAVE_HIBERNATE_URL, bancoURL);
                propriedades.put(CHAVE_HIBERNATE_USERNAME, bancoUsuario);
                propriedades.put(CHAVE_HIBERNATE_PASSWORD, bancoSenha);
                propriedades.put(CHAVE_RETAGUARDA, RETAGUARDA_ALTERDATA_SQLSERVER_RETAGUARDA);

                 {
                    String cfops = textoCFOPs.getText();
                    propriedades.put(CHAVE_ALTERDATA_CFOPS, cfops);
                    String estadosEntrada = textoEstadosEntrada.getText();
                    propriedades.put(CHAVE_ALTERDATA_ESTADOS_ENTRADA, estadosEntrada);
                    String estadosSaida = textoEstadosSaida.getText();
                    propriedades.put(CHAVE_ALTERDATA_ESTADOS_SAIDA, estadosSaida);
                }
                break;
        }
        try {
            propriedades.store(new FileOutputStream(arquivoConfiguracoesLocal), null);
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Um erro ocorreu ao tentar criar um arquivo de configurações. Entre em contato com o suporte do alerta fiscal.");
            System.exit(0);
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void botaoArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoArquivoActionPerformed
        JFileChooser escolhaDeArquivo = new JFileChooser();
        escolhaDeArquivo.setCurrentDirectory(new File(System.getProperty("user.home")));
        int resultado = escolhaDeArquivo.showOpenDialog(this);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            File arquivoSelecionado = escolhaDeArquivo.getSelectedFile();
            textoArquivo.setText(arquivoSelecionado.getAbsolutePath());
        }
    }//GEN-LAST:event_botaoArquivoActionPerformed

    private String obtemValorSelecionado(JComboBox jComboBox) {
        DefaultComboBoxModel model = (DefaultComboBoxModel) jComboBox.getModel();
        return String.valueOf(model.getSelectedItem());
    }

