    private void comboBoxTipoEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxTipoEntradaActionPerformed
        String valor = obtemValorSelecionado(comboBoxTipoEntrada);
        if (TIPO_ENTRADA_XML.equals(valor)) {
            painelRevisaoXML.setEnabled(true);
            textoArquivo.setEnabled(true);
            painelRevisaoWebService.setEnabled(false);
            textoID.setEnabled(false);
            textoToken.setEnabled(false);
            textoURL.setEnabled(false);
        } else if (TIPO_ENTRADA_WEBSERVICE.equals(valor)) {
            painelRevisaoXML.setEnabled(false);
            textoArquivo.setEnabled(false);
            painelRevisaoWebService.setEnabled(true);
            textoID.setEnabled(true);
            textoToken.setEnabled(true);
            textoURL.setEnabled(true);
        }
    }//GEN-LAST:event_comboBoxTipoEntradaActionPerformed

